    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String consulta = JOptionPane.showInputDialog("Digite a Pergunta");
        
        Integer posicaoachou = 0;
        Boolean encontrou = false;
        
        if(consulta.isEmpty() == false)
        {
            for (Pergunta pergunta : lista){
                
                if(consulta.equals(pergunta.getEnunciado()))
                {
                   encontrou = true; 
                   txtEnunciado.setText(pergunta.getEnunciado());
                   txtA.setText(pergunta.getA());
                   txtB.setText(pergunta.getB());
                   txtC.setText(pergunta.getC());
                   txtD.setText(pergunta.getD());
                   txtCerta.setText(pergunta.getCerta());
      
                   posicao=posicaoachou;
                   break;
                }
                posicaoachou ++; 
            }
        }
        if(encontrou == false)
        {
            JOptionPane.showMessageDialog(rootPane, "Pergunta não Encontrada");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

