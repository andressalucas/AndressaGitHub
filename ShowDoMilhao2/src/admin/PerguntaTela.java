    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        //Precisa do codigo na chave primária
        if (txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog (null, "Registro não informado para ser atualizado");
        } else {
            Pergunta pergunta = new Pergunta();
            pergunta.setEnunciado(txtEnunciado.getText());
            pergunta.setA(txtA.getText());
            pergunta.setB(txtB.getText());
            pergunta.setC(txtC.getText());
            pergunta.setD(txtD.getText());
            pergunta.setCerta(txtCerta.getText());
            pergunta.setNivel(txtNivel.getText());
            
            PerguntaDAO dao = new PerguntaDAO();
            boolean deucerto;
            deucerto = dao.atualizar(pergunta);
            if(deucerto == true){
                JOptionPane.showMessageDialog(null, "Registro cadastrado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar o registro");
            }
            lista = dao.listar();
        }
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

