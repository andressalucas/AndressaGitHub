    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        //Precisa do codigo na chave primária
        if (txtLogin.getText().isEmpty()){
            JOptionPane.showMessageDialog (null, "Registro não informado para ser atualizado");
        } else {
            Jogador jogador = new Jogador();
            jogador.setLogin(txtLogin.getText());
            jogador.setEmail(txtEmail.getText());
            jogador.setSenha(txtSenha.getText());
            
            JogadorDAO dao = new JogadorDAO();
            boolean deucerto;
            deucerto = dao.atualizar(jogador);
            if(deucerto == true){
                JOptionPane.showMessageDialog(null, "Registro atualizado com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao alterar o registro");
            }
            lista = dao.listar();
        }
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

