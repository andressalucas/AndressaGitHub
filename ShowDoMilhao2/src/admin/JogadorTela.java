    private void btnPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrimeiroActionPerformed
        if(lista.size() > 0)
        {
            posicao = 0; 
            
            Jogador jogador = lista.get(posicao);
            
            txtLogin.setText(jogador.getLogin());
            txtSenha.setText(jogador.getSenha());
            txtEmail.setText(jogador.getEmail());
            
        }
    }//GEN-LAST:event_btnPrimeiroActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        if(posicao > 0)
        {
            posicao = posicao - 1; 
        
            Jogador jogador = lista.get(posicao);
        
            txtLogin.setText(jogador.getLogin());
            txtSenha.setText(jogador.getSenha());
            txtEmail.setText(jogador.getEmail());
            
        }
    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        if(posicao < lista.size() - 1)
        {
            posicao = posicao + 1; 
            
            Jogador jogador = lista.get(posicao);
            
            txtLogin.setText(jogador.getLogin());
            txtSenha.setText(jogador.getSenha());
            txtEmail.setText(jogador.getEmail());
            
        }
    }//GEN-LAST:event_btnProximoActionPerformed

    private void btnUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUltimoActionPerformed
        if(lista.size() > 0)
        {
            posicao = lista.size() - 1; 
            
            Jogador jogador = lista.get(posicao);
            
            txtLogin.setText(jogador.getLogin());
            txtSenha.setText(jogador.getSenha());
            txtEmail.setText(jogador.getEmail());
            
        }
    }//GEN-LAST:event_btnUltimoActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Jogador item = new Jogador(); 
        
        if(txtLogin.getText().isEmpty() || txtSenha.getText().isEmpty() || txtEmail.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Todos os campos são obrigatorios");
        }
        else
        {
                item.setLogin(txtLogin.getText());
                item.setSenha(txtSenha.getText());
                item.setEmail(txtEmail.getText());
                
                JogadorDAO dao = new JogadorDAO ();
                Boolean deucerto = dao.cadastrar(item);
                if (deucerto == true){
                    JOptionPane.showMessageDialog(rootPane, "Cadastro com sucesso");
                    
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Erro ao cadastro");
                }
               
                
                JOptionPane.showMessageDialog(rootPane, "Cadastro com Sucesso");    
        }
         lista.add(item);
         posicao ++;
        Limpar();
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

